package com.company.if_Switch_for_continue;

public class for01 {
    public static void main(String[] args) {
        for (int i = 0; i <20;i++) {
            System.out.println("lala");
        }
        System.out.println("============");
        int j=0;
        for (;j<=20;){
            System.out.println("dddddd");
            j++;
        }
        System.out.println(j);

        for (;;){
            System.out.println("lllllllllll");
        }
    }
}
